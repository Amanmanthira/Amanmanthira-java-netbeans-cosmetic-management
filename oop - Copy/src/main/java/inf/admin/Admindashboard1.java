    private ArrayList<String> loadList(String filename) {
    ArrayList<String> list = new ArrayList<>();
    try (BufferedReader reader = new BufferedReader(new FileReader(filename))) {
        String line;
        while ((line = reader.readLine()) != null) {
            list.add(line.trim());
        }
    } catch (IOException e) {
        e.printStackTrace();
    }
    return list;
}


