    private void searchbynameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchbynameActionPerformed
        String keyword = searchboxemployfield.getText().trim().toLowerCase(); // Trim and convert to lowercase

        ArrayList<String> results = new ArrayList<>();
        try (BufferedReader reader = new BufferedReader(new FileReader("Products.txt"))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String[] parts = line.split(",");
                for (String part : parts) {
                    if (part.toLowerCase().contains(keyword)) { // Convert to lowercase for case-insensitive search
                        results.add("Name: " + parts[0] + ", Category: " + parts[1] + ", For: " + parts[2] + ", Price: " + parts[3] + ", Descrition: " + parts[4]  );
                        break; // No need to continue searching in this line once found
                    }
                }
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "An error occurred while searching for employees.", "Error", JOptionPane.ERROR_MESSAGE);
            return; // Return from the method to avoid further execution
        }

        if (!results.isEmpty()) {
            StringBuilder message = new StringBuilder("Search results for '" + keyword + "':\n");
            for (String result : results) {
                message.append(result).append("\n");
            }
            JOptionPane.showMessageDialog(null, message.toString(), "Search Results", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "No matching results found.", "Search Results", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_searchbynameActionPerformed

