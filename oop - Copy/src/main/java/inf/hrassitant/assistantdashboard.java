    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         String filePath = "Products.txt";
    File file = new File(filePath);

    try (FileReader fr = new FileReader(file);
         BufferedReader br = new BufferedReader(fr)) {

        DefaultTableModel model = (DefaultTableModel) producttable.getModel();
        model.setRowCount(0); // Clear existing table rows

        String line;
        while ((line = br.readLine()) != null) {
            String[] row = line.split(",");
            if (row.length == 4) { // Adjust the length according to your data structure
                model.addRow(row);
            } else {
                System.out.println("Skipping line: " + line);
            }
        }

    } catch (FileNotFoundException ex) {
        Logger.getLogger(JTable_import_and_export_to_text_file.class.getName()).log(Level.SEVERE, null, ex);
    } catch (IOException ex) {
        Logger.getLogger(JTable_import_and_export_to_text_file.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_jButton2ActionPerformed

