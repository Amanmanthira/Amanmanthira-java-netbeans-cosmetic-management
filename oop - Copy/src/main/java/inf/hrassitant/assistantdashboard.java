    private void addnewproductsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addnewproductsActionPerformed
        String newProductName = newproductnamefield.getText();
        String newCategory = newproductcategory.getSelectedItem().toString();
        String newProductsFor = jComboBox2.getSelectedItem().toString();
        String newProductPrice = priceboxfield.getText();

        // Check if any required field is empty
        if (newProductName.isEmpty() || newCategory.isEmpty() || newProductPrice.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill in all required fields");
            return; // Exit the method without writing to the file
        }

        // Validate that price is a number
        if (!newProductPrice.matches("\\d+(\\.\\d{1,2})?")) {
            JOptionPane.showMessageDialog(null, "Please enter a valid price");
            return; // Exit the method if price is not valid
        }

        try (BufferedWriter writer = new BufferedWriter(new FileWriter("Products.txt", true))) {
            writer.write(newProductName + "," + newCategory + "," + newProductsFor + "," + newProductPrice + "," +  "\n");
            JOptionPane.showMessageDialog(null, "Product added successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Failed to add product", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_addnewproductsActionPerformed

